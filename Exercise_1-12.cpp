/*
Exercise 1.12: What does the following for loop do? What is the final value of sum?
int sum = 0;
for (int i = -100; i <= 100; ++i)
	sum += i;
*/

/*
	Answer:
		It loops from -100 to 100 and adds the current number to the sum.
		In the end it will return zero as the loop is equivalent to 100-100.

*/